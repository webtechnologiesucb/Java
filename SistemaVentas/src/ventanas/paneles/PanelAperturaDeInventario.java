    private void bntGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGrabarActionPerformed
        this.aceptado = true;
        if(!esActualizacion)
        {
            kardex.setProducto(prd);
            kardex.setFecha(Calendar.getInstance().getTime());
            kardex.setDocumento(Kardex.APERTURA);
            kardex.setNumeroDeDocumento("000000");
            kardex.setEntrada(1); 
        }else{
            controllerKardex = new CKardex();
            kardex = (Kardex)controllerKardex.buscarRegistro(new Object[]{Kardex.APERTURA,prd.getPrimaryKey()});
        }
        getOptionPane();
        op.setValue(1);
    }//GEN-LAST:event_bntGrabarActionPerformed

    
    public int grabarDatos()
    {
        ModeloTablaApertura mta = (ModeloTablaApertura)this.tblAlmacencantidades.getModel();
        ArrayList<AlmacenProducto> datos = mta.getDatos();
        controllerAp = new CAlmacenProducto();
        controllerKardex = new CKardex();
        if(kardex != null)
        {
            kardex.setEntrada(mta.getTotalProductos());
            if(!esActualizacion)
            {
                boolean guardarRegistro = controllerKardex.guardarRegistro(kardex); 
            }else{
                controllerKardex.actualizarRegistro(kardex);
            }
            
        }
        
        int actualizarRegistro = 0;
        for(AlmacenProducto ap:datos)
        {
            actualizarRegistro = controllerAp.actualizarRegistro(ap);           
        }
        return actualizarRegistro; 
    }
    
