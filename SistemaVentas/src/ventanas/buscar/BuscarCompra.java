    private void btnUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUltimoActionPerformed
        Paginador paginar = new Paginador();  
        paginar.setLimite(this.paginador.getLimite()); 
        paginar.setTipoObjeto(new Compra());        
        int dezplazamiento = paginar.getCantidadesDePaginas()*paginar.getLimite(); 
        paginar.setIndexpaginaActual(paginar.getCantidadesDePaginas());
        paginar.setDesplazamiento(dezplazamiento);
        buscar(paginar);
        this.btnSiguiente.setEnabled(false);
        this.btnUltimo.setEnabled(false);
        this.btnPrimero.setEnabled(true);
        btnAnterior.setEnabled(true);
    }//GEN-LAST:event_btnUltimoActionPerformed

    private void buscar(Paginador paginar)
    {
        this.paginador = paginar;
        paginador.setTipoObjeto(new Compra());
        paginador.setLimite(3);  
        CriterioSQL criterio = paginador.getCriterio();
        criterio.setTabla(Compra.nt);
        if(!this.txtNroDocumento.getText().isEmpty())
        {
            criterio.addCondicion("numdoc", this.txtNroDocumento.getText(), "=");
        }
        
        if(this.txtPrecioMayor.getValue() != null && this.txtPrecioMenor.getValue() != null)
        {
            criterio.addCompareValorBetween("total", this.txtPrecioMayor.getValue(), this.txtPrecioMenor.getValue(), "and");
        }else
        {
            if(this.txtPrecioMayor.getValue() != null)
            {
                criterio.addCondicion("total", this.txtPrecioMayor.getValue(),">=");
            }

            if(this.txtPrecioMenor.getValue()!=null)
            {
                criterio.addCondicion("total", this.txtPrecioMenor.getValue(),"<=");
            }
            System.out.println(this.txtPrecioMenor.getValue());
        }
        
        if((Integer)this.spItems.getValue() > 0)
        {
            criterio.addCondicion("numitems", (Number)this.spItems.getValue());
        }
        
        if(this.fcAntes.getCalendar() !=null && this.fcDespues.getCalendar()!=null)
        {
            criterio.addCompareValorBetween("fecha", Helper.getFechaFormateada(this.fcDespues.getCalendar().getTime(), Helper.ANIO_MES_DIA), Helper.getFechaFormateada(this.fcAntes.getCalendar().getTime(), Helper.ANIO_MES_DIA), "and");
        }else
        {
            if(this.fcAntes.getCalendar() !=null)
            {
                criterio.addCompareTexto("fecha", Helper.getFechaFormateada(this.fcAntes.getCalendar().getTime(), Helper.ANIO_MES_DIA),"and","<=");
            }
            if(this.fcDespues.getCalendar()!=null)
            {
                criterio.addCompareTexto("fecha", Helper.getFechaFormateada(this.fcDespues.getCalendar().getTime(), Helper.ANIO_MES_DIA),"and",">=");
            }
        }
        mtc = new ModeloTablaCompra(paginador);
        this.tblCompras.setModel(mtc);
        System.out.println(criterio.getConsultaSQL());
    }
    
    
